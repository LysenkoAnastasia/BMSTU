    public static int[][] simpleMatrix(int match) {
        int[][] m = new int[4][4];

        for (int i = 0; i < 4; i++) {
            for (int j = 0; j < 4; j++) {

                m[i][j] = i == j ? match : Integer.MIN_VALUE/2;
            }
        }
        return m;
    }

